package com.jilit.irp.controller;

import com.jilit.irp.iservice.RegistrationSummaryReportIservice;
import java.util.HashMap;
import java.util.List;
import java.util.Map;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.ResponseBody;

/**
 *
 * @author ashutosh1.kumar
 */
@Controller
@RequestMapping("/registrationSummaryReport")
public class RegistrationSummaryReportController {
      
    @Autowired
    RegistrationSummaryReportIservice regSummaryReportIservice;
    
    @RequestMapping("/list")
    public String getregistrationSummaryReportList(Model model) {
        regSummaryReportIservice.getFormData(model);
        return "registrationSummaryReport/registrationSummaryReportList";
    }
    
    @RequestMapping("/getAcademicYear")
    @ResponseBody
    public Map getAcademicYear(HttpServletRequest request) {
        List l = regSummaryReportIservice.getAcademicYear(request);
        Map map = new HashMap();
        map.put("acadYearList", l);
        return map;
    }
    
    @RequestMapping("/getReport")
    public void getTeachingSchemeReport(HttpServletRequest request, HttpServletResponse response) {
        regSummaryReportIservice.getReport(request, response);
    }
    
}
